public class NodesNotConnectedException extends Exception {
	public NodesNotConnectedException() {
		super();
	}

	public NodesNotConnectedException(String message, Throwable cause) {
		super(message, cause);
	}

	public NodesNotConnectedException(String message) {
		super(message);
	}

	public NodesNotConnectedException(Throwable cause) {
		super(cause);
	}
}