public class PositionsNotConnectedException extends Exception {
	public PositionsNotConnectedException() {
		super();
	}

	public PositionsNotConnectedException(String message, Throwable cause) {
		super(message, cause);
	}

	public PositionsNotConnectedException(String message) {
		super(message);
	}

	public PositionsNotConnectedException(Throwable cause) {
		super(cause);
	}
}